#include <stdio.h>

/**
 * add-add function
 * Return: a + b
 * @a:a
 * @b:b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub-subtract function
 * Return: a - b
 * @a:a
 * @b:b
 */
int sub(int a, int b)
{
        return (a - b);
}
/**
 * mul-multiply function
 * Return: a * b
 * @a:a
 * @b:b
 */
int mul(int a, int b)
{
        return (a * b);
}
/**
 * div-divide function
 * Return: a / b
 * @a:a
 * @b:b
 */
int div(int a, int b)
{
        return (a / b);
}  
/**
 * mod-modulus function
 * Return: a % b
 * @a:a
 * @b:b
 */
int mod(int a, int b)
{
        return (a % b);
}
